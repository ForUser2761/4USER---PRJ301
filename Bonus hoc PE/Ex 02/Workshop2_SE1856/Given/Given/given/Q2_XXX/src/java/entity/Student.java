package entity;

/**
 *
 * @author ADMIN
 */
public class Student {
    private String code;
    private String name;
    private String dob;
    private String gender;
    private String major;

    public Student(String code, String name, String dob, String gender, String major) {
        this.code = code;
        this.name = name;
        this.dob = dob;
        this.gender = gender;
        this.major = major;
    }

    // Getters and Setters
    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDob() {
        return dob;
    }

    public void setDob(String dob) {
        this.dob = dob;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getMajor() {
        return major;
    }

    public void setMajor(String major) {
        this.major = major;
    }
}
