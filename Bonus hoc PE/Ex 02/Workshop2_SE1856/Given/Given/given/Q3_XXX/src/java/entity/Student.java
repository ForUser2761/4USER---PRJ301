package entity;

/**
 *
 * @author ADMIN
 */
public class Student {

    private String id;
    private String name;
    private String dob;
    private String gender;
    private String major;

    // Constructor, Getters, Setters
    public Student(String id, String name, String dob, String gender, String major) {
        this.id = id;
        this.name = name;
        this.dob = dob;
        this.gender = gender;
        this.major = major;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDob() {
        return dob;
    }

    public void setDob(String dob) {
        this.dob = dob;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getMajor() {
        return major;
    }

    public void setMajor(String major) {
        this.major = major;
    }
}
