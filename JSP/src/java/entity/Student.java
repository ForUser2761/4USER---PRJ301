package entity;

/**
 *
 * @author ADMIN
 */
public class Student {
    private int stt;
    private String name;
    private int age;

    public Student() {
    }

    public Student(int stt, String name, int age) {
        this.stt = stt;
        this.name = name;
        this.age = age;
    }

    public int getStt() {
        return stt;
    }

    public void setStt(int stt) {
        this.stt = stt;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
    
    
}
